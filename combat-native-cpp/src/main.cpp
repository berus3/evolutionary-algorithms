// helpers para imprimir
static double mean(const std::vector<double>& v) {
    if (v.empty()) return 0.0;
    return std::accumulate(v.begin(), v.end(), 0.0) / static_cast<double>(v.size());
}

static std::vector<size_t> topk_indices(const std::vector<double>& v, size_t k) {
    std::vector<size_t> idx(v.size());
    std::iota(idx.begin(), idx.end(), 0);
    if (k > idx.size()) k = idx.size();
    std::partial_sort(
        idx.begin(), idx.begin() + k, idx.end(),
        [&](size_t a, size_t b) { return v[a] > v[b]; }
    );
    idx.resize(k);
    return idx;
}

constexpr std::size_t GENOME_SIZE = 500;
std::array<int, GENOME_SIZE> parseGenomeArray(const std::string& genomeStr) {
    std::array<int, GENOME_SIZE> genome{};
    std::istringstream iss(genomeStr);

    for (std::size_t i = 0; i < GENOME_SIZE; ++i) {
        if (!(iss >> genome[i])) {
            throw std::runtime_error("Genome incompleto o inv√°lido");
        }
    }

    return genome;
}

std::vector<std::array<int, GENOME_SIZE>> loadGenomesFromCsv() {
    const std::string path = "../../evolver-java/logs/a.csv";

    std::ifstream file(path);
    if (!file.is_open()) {
        throw std::runtime_error("No se pudo abrir el archivo CSV");
    }

    std::vector<std::array<int, GENOME_SIZE>> genomes;
    std::string line;

    std::getline(file, line);

    while (std::getline(file, line)) {
        if (line.empty()) continue;

        std::stringstream ss(line);
        std::string rankStr, fitnessStr, winrateStr, genomeStr;

        std::getline(ss, rankStr, ',');
        std::getline(ss, fitnessStr, ',');
        std::getline(ss, winrateStr, ',');
        std::getline(ss, genomeStr);

        genomes.push_back(parseGenomeArray(genomeStr));
    }

    return genomes;
}


